    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->file('file')->getClientOriginalName();
        $file = $request->file('file')->store('public/products');

        $host = $_SERVER['HTTP_HOST'];
        $imgName = explode('/',$file)[2];
        $img_location = "http://".$host."/storage/products/".$imgName;

        $pdt = new Product;
        $pdt->name = $request->name;
        $pdt->price = $request->price;
        $pdt->category = $request->category;
        $pdt->image = $img_location;
        $pdt->created_at = Carbon::now();
        $pdt->updated_at = Carbon::now();
        $pdt->save();

        return response()->json('Success');
    }

